                    <div class="col-lg-6 col-mg-6 col-ms-12 col-xs-12 about_me_left_section">
                        <p id="txt">The Kammavari Sangham, established in the year 1952, is a non-profit oriented voluntary service organization. The organization was created with the objective of providing charitable service to the community and society. The Sangham has diversified its activities since its establishment over five decades ago. Now it has expanded its horizons to include community-strengthening ventures, providing education, mechanical and financial support to the society.</p>
                        <p id="txt">With a firm belief that quality education only can lay a strong foundation for bringing about economic and social changes to the lives of millions, the Sangham went about establishing educational institutions, starting with K.S. Polytechnic in 1992. Enthused with this success of its foray into technical education, the Sangham then started the K.S Institute of Technology (KSIT), its first Engineering College in the year 1999. In the following years both these institutions have carved for themselves an enviable niche through academic excellence achieved in a very short span of time. Following the success of KSIT, the Sangham ventured onwards to start yet another technical institution, oriented towards advanced studies and research, and thus, the K.S Group of Institutions (KSGI) was born in 2010. KSGI was renamed as K.S.School of Engineering & Management in 2011.</p>
                    </div>
                    <div class="col-lg-6 col-mg-6 col-ms-12 col-xs-12 about_me_right_section">
                        <p id="txt">By providing FREE hostel accommodation and scholarship to the deserving students in the community, it has furthered its commitment to education.</p>
                        <p id="txt">Apart from the educational initiatives, on the cultural front, the Sangham has ventured to build and manage Convention centers. These serve as venues for community cultural events including conducting of mass marriages among the economically underprivileged in the Kamma Community.</p>
                        <p id="txt">To encourage and nourish entrepreneurial trait among the community members, the Sangham also started Kammavari Credit Co-operative Society, to mobilize deposits and finance enterprises.</p>
                        <p id="txt">To be of service to the sick and suffering, the Sangham has established a modern hospital in Bangalore, with the latest diagnostic and treatment services to help the community and the society at large.</p>
